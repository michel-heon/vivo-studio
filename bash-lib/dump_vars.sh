# Email         : heon.michel@uqam.ca
###################################################################
echo "###################################################################"
echo "### Parametre AWS #################################################"
echo "###################################################################"
[ $AWS_DEFAULT_PROFILE ] && echo "# AWS_DEFAULT_PROFILE = ($AWS_DEFAULT_PROFILE)"
[ $AWS_USERNAME ]        && echo "# AWS_USERNAME        = ($AWS_USERNAME)"
[ $AWS_GROUP ]           && echo "# AWS_GROUP           = ($AWS_GROUP)"
echo "###################################################################"
echo "### Vars. des ressources à déployer ################################"
echo "###################################################################"
[ $VIVO_STAGE ]         && echo "# VIVO_STAGE         = ($VIVO_STAGE)"
[ $VIVO_CNAME ]         && echo "# VIVO_CNAME         = ($VIVO_CNAME)"
[ $VP_CNAME ]           && echo "# VP_CNAME           = ($VP_CNAME)"
[ $SEARCH_CNAME ]         && echo "# SEARCH_CNAME         = ($SEARCH_CNAME)"
[ $DATASRC_CNAME ]      && echo "# DATASRC_CNAME      = ($DATASRC_CNAME)"
[ $POPULATOR_CNAME ]    && echo "# POPULATOR_CNAME    = ($POPULATOR_CNAME)"
[ $VIVO_CNAME_EBS_ENV ] && echo "# VIVO_CNAME_EBS_ENV = ($VIVO_CNAME_EBS_ENV)"
[ $VP_CNAME_EBS_ENV ]   && echo "# VP_CNAME_EBS_ENV   = ($VP_CNAME_EBS_ENV)"
[ $SEARCH_CNAME_EBS_ENV ] && echo "# SEARCH_CNAME_EBS_ENV = ($SEARCH_CNAME_EBS_ENV)"
[ $CNAME ]              && echo "# CNAME              = ($CNAME)"
[ $TOMCAT_HOME ]        && echo "# TOMCAT_HOME        = ($TOMCAT_HOME)"
echo "###################################################################"
echo "### Liste des paquetages ###########################################"
echo "###################################################################"
echo "# VIVO_PKG   = ($VIVO_PKG)"
echo "# VITRO_PKG  = ($VITRO_PKG)"
echo "# SEARCH_PKG = ($SEARCH_PKG)"
echo "# SAMPLE_PKG = ($SAMPLE_PKG)"
echo "###################################################################"
echo "### Liste des bundles #############################################"
echo "###################################################################"
echo "# SEARCH_BUNDLE = ($SEARCH_BUNDLE)"
echo "###################################################################"
echo "###################################################################"
echo "### Config du VPC AWS          ####################################"
echo "###################################################################"
echo "# VPC_NAME                     = ($VPC_NAME)"
echo "# DC_VPC_ID                    = ($DC_VPC_ID)"
echo "# DC_SUBNET_PUBLIC_ID_1        = ($DC_SUBNET_PUBLIC_ID_1)"
echo "# DC_SUBNET_PUBLIC_ID_2        = ($DC_SUBNET_PUBLIC_ID_2)"
echo "# DC_SUBNET_PRIVATE_ID_1       = ($DC_SUBNET_PRIVATE_ID_1)"
echo "# DC_SUBNET_PRIVATE_ID_2       = ($DC_SUBNET_PRIVATE_ID_2)"
echo "# DC_INTERNET_GATEWAY_ID       = ($DC_INTERNET_GATEWAY_ID)"
echo "# DC_DEFAULT_SECURITY_GROUP_ID = ($DC_DEFAULT_SECURITY_GROUP_ID)"
echo "# DC_SERVICE_ROLE              = ($DC_SERVICE_ROLE)"
echo "# DC_DEFAULT_ROUTE_TABLE_ID    = ($DC_DEFAULT_ROUTE_TABLE_ID)"
echo "# CLE_SSH                      = ($CLE_SSH)"
echo "###################################################################"
echo "### Vars. INSTALLATION         ####################################"
echo "###################################################################"
[ -z $INSTALL_EFS ]         || echo "# INSTALL_EFS         = ($INSTALL_EFS)"
[ -z $INSTALL_SAMPLE ]      || echo "# INSTALL_SAMPLE      = ($INSTALL_SAMPLE)"
[ -z $INSTALL_NEPTUNE ]     || echo "# INSTALL_NEPTUNE     = ($INSTALL_NEPTUNE)"
[ -z $INSTALL_SOLR_EBS ]    || echo "# INSTALL_SOLR_EBS    = ($INSTALL_SOLR_EBS)"
[ -z $INSTALL_FUSEKI ]      || echo "# INSTALL_FUSEKI      = ($INSTALL_FUSEKI)"
[ -z $INSTALL_VIVO_STUDIO ] || echo "# INSTALL_VIVO_STUDIO = ($INSTALL_VIVO_STUDIO)"
echo "###################################################################"
echo "### ENV (S3/Neptune/EFS) STORAGE ##################################"
echo "###################################################################"
[ -z $INSTALL_NEPTUNE ] && export INSTALL_NEPTUNE=false
[ $INSTALL_NEPTUNE ] || echo "# NEPTUNE_BUCKET_PARAM   = ($NEPTUNE_BUCKET_PARAM)"
[ $INSTALL_NEPTUNE ] || echo "# NEPTUNE_BUCKET_ROOT    = ($NEPTUNE_BUCKET_ROOT)"
[ $INSTALL_NEPTUNE ] || echo "# NEPTUNE_CLUSTER_NAME   = ($NEPTUNE_CLUSTER_NAME)"
[ $INSTALL_NEPTUNE ] || echo "# NEPTUNE_INSTANCE_NAME  = ($NEPTUNE_INSTANCE_NAME)"
[ $INSTALL_NEPTUNE ] || echo "# NEPTUNE_GROUP_NAME     = ($NEPTUNE_GROUP_NAME)"
[ $INSTALL_NEPTUNE ] || echo "# NEPTUNE_SPARQL_RO      = ($NEPTUNE_SPARQL_RO)"
[ $INSTALL_NEPTUNE ] || echo "# NEPTUNE_SPARQL_RW      = ($NEPTUNE_SPARQL_RW)"
[ $INSTALL_NEPTUNE ] || echo "# NEPTUNE_SPARQL_PORT    = ($NEPTUNE_SPARQL_PORT)"
[ $INSTALL_NEPTUNE ] || echo "# NEPTUNE_ENDPOINT       = ($NEPTUNE_ENDPOINT)"
[ $INSTALL_EFS ] || echo "# EFS_NAME               = ($EFS_NAME)"
echo "###################################################################"
echo "### Vars. de VIVO #################################################"
echo "###################################################################"
echo "# VIVO                  = ($VIVO)"
echo "# VIVO_THEME            = ($VIVO_THEME)"
echo "# VIVO_RELEASE          = ($VIVO_RELEASE)"
echo "# VIVO_DIST_RELEASE     = ($VIVO_DIST_RELEASE) (vivo-vitro version)"
echo "# VIVO_UQAM_RELEASE     = ($VIVO_UQAM_RELEASE)"
echo "# VIVO_DEFAULT_NS_SED   = ($VIVO_DEFAULT_NS_SED)"
echo "# EBS_HOME              = ($EBS_HOME)"
echo "# VIVO_HOME_PROPERTIES  = ($VIVO_HOME_PROPERTIES)"
echo "# RUNTIME_PROPERTIES    = ($RUNTIME_PROPERTIES)"
echo "# APPLICATION_SETUP_N3  = ($APPLICATION_SETUP_N3)"
echo "# VIVO_INSTALLER_HOME   = ($VIVO_INSTALLER_HOME)"
echo "# SOLR_URL              = ($SOLR_URL)"
echo "# SOLR_REP              = ($SOLR_REP)"
echo "# SEARCH_URL            = ($SEARCH_URL)"
echo "###################################################################"
echo "###################################################################"
echo ""


